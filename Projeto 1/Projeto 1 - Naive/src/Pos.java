// Classe auxiliar para a posição do espaço vazio.
class Pos {
	int lin;
	int col;
	
	Pos(int lin, int col) {
		this.lin = lin;
		this.col = col;
	}
	
	public void setPos(int lin, int col) {
		this.lin = lin;
		this.col = col;
	}
}
